    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Coche coche = Gestion.mostrarCoche(jTextField1.getText());
            jTextField2.setText(coche.getColor().name());
            jTextField3.setText(coche.getModelo().name());
            jTextField4.setText(coche.getMarca().name());
        } catch (CocheNoExisteException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "error al encontrar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

